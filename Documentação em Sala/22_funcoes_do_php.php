<?php
$name = 'Senac Americana -  Curso de Ti';
echo strtoupper($name) . "<br>"; // deixa tudo em maiúsculo
echo '<hr>';
echo strtolower($name) . "<br>"; // deixa tudo em minúsculo
echo '<hr>';
echo ucfirst(strtolower($name)) . "<br>"; // deixa a primeira letra maiúscula e o resto minúsculo
echo '<hr>';
echo ucwords(strtolower($name)) . "<br>"; // deixa a primeira letra de cada palavra maiúscula e o resto minúsculo
echo '<hr>';


































?>

